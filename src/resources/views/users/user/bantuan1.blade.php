<div class="card border-success my-3">
    <div class="card-header bg-transparent border-success">
        <h3>Bantuan Membuat laporan</h3>
    </div>

    <div class="card-body">
        <h5 class="card-title">1. Masuk ke profil</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan1.png" alt="">
        <p class="card-text" style="text-align: justify;">Langkah pertama dalam proses pelaporan adalah masuk ke profil anda. Untuk melakukan ini, klik pada bagian yang menampilkan "Selamat Datang", kemudian pilih "Profil". Di dalam menu profil, anda akan menemukan berbagai fitur, termasuk fitur pelaporan. Pastikan anda sudah login ke akun anda sebelum melanjutkan ke langkah ini.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">2. Permohonan</h5>
        <img class="mx-auto d-block img-fluid img-tutorial20" src="/img/pelaporan/pelaporan2.png" alt="">
        <p class="card-text" style="text-align: justify;">Di menu profil, anda akan menemukan opsi untuk melakukan permohonan RTH. Klik pada menu ini untuk memulai proses permohonan. Pastikan anda membaca semua instruksi dengan teliti untuk memastikan permohonan anda lengkap.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">3. Klik jalur</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan3.png" alt="">
        <p class="card-text" style="text-align: justify;">Pada halaman permohonan, anda akan melihat peta dengan berbagai jalur berwarna. Klik pada jalur yang berwarna biru untuk memulai permohonan. Jalur akan berubah warna menjadi hijau menandakan bahwa telah dipilih. Jalur berwarna merah tidak termasuk dalam tanggung jawab DLH dan permohonan tidak dapat diajukan untuk area tersebut.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">4. Klik mendapatkan koordinat</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan4.png" alt="">
        <p class="card-text" style="text-align: justify;">Setelah memilih jalur, klik tombol "Klik untuk mendapatkan koordinat" untuk mendapatkan informasi lintang dan bujur lokasi yang akan digunakan dalam data permohonan. Informasi ini sangat penting untuk memastikan bahwa lokasi permohonan anda tepat dan dapat diproses dengan cepat.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">5. Klik lokasi peta</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan5.png" alt="">
        <p class="card-text" style="text-align: justify;">Klik pada peta untuk menentukan lokasi spesifik dari permohonan anda. Dianjurkan untuk mengklik di sebelah jalur yang telah anda pilih sebelumnya. Pastikan lokasi yang anda pilih akurat, seperti yang ditunjukkan pada gambar, untuk menghindari kesalahan dalam pemrosesan permohonan.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">6. Isi form</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan6.png" alt="">
        <p class="card-text" style="text-align: justify;">Isi formulir permohonan dengan informasi yang diperlukan seperti NIK, lokasi tertulis, dan keterangan tambahan. Lokasi tertulis bisa mencakup detail lebih lanjut lokasi. Pastikan semua informasi yang anda masukkan benar dan lengkap.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">7. Tambahkan foto</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan7.png" alt="">
        <p class="card-text" style="text-align: justify;">Sertakan bukti foto pohon sekitar, lampiran rekomendasi dari kapanewon, dan fotokopi KTP untuk verifikasi identitas. Bukti foto ini penting untuk mendukung permohonan anda dan memastikan bahwa kondisi di lapangan sesuai dengan yang dilaporkan. Setelah semua dokumen terlampir, klik tombol "Submit" untuk mengirim permohonan anda.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">8. Berhasil</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan8.png" alt="">
        <p class="card-text" style="text-align: justify;">Setelah mengirim permohonan, anda akan menerima konfirmasi bahwa permohonan anda telah berhasil ditambahkan. Anda dapat memantau status permohonan dan melakukan filter berdasarkan kode, status permohonan, atau tanggal dibuat untuk melihat perkembangan lebih lanjut.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">9. Daftar permohonan</h5>
        <img class="mx-auto d-block img-fluid img-tutorial20" src="/img/pelaporan/pelaporan9.png" alt="">
        <p class="card-text" style="text-align: justify;">Pada menu "Daftar Permohonan", anda akan menemukan kumpulan semua permohonan yang telah anda laporkan. Di sini, anda dapat melihat detail setiap permohonan dengan mengklik tombol "Detail" yang tersedia di setiap permohonan. Menu ini sangat berguna untuk memantau semua permohonan anda.</p>
    </div>
    <div class="card-body">
        <h5 class="card-title">10. Detail permohonan</h5>
        <img class="mx-auto d-block img-fluid img-tutorial70" src="/img/pelaporan/pelaporan10.png" alt="">
        <p class="card-text" style="text-align: justify;">Pada halaman detail permohonan, anda dapat melihat semua informasi yang telah diisi sebelumnya. Halaman ini juga menampilkan status permohonan anda, apakah sedang diproses, menunggu, atau sudah selesai. Terdapat 6 status, yaitu:</p>
        <ul class="list-unstyled">
            <li>1. Terdapat data yang salah</li>
            <li>2. Menunggu diproses</li>
            <li>3. Sedang disurvei</li>
            <li>4. Menunggu surat rekomendasi keluar</li>
            <li>5. Sedang dilaksanakan</li>
            <li>6. Selesai</li>
        </ul>
    </div>
